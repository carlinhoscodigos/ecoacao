// Utilitário: normaliza texto para busca (remove acentos, lowercase)
export function normalizarCidade(str) {
  return str
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9 ]/g, ' ')
    .trim();
}

// Busca cidade pelo nome (normalizado)
export function buscarCidade(query) {
  const q = normalizarCidade(query);
  if (!q) return [];
  return RS_CITIES.filter((c) => normalizarCidade(c.nome).includes(q)).slice(0, 15);
}

// Busca cidade pelo nome retornando correspondência exata
export function encontrarCidade(nome) {
  const q = normalizarCidade(nome);
  return RS_CITIES.find((c) => normalizarCidade(c.nome) === q) || null;
}
